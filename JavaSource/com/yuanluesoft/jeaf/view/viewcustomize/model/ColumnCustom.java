package com.yuanluesoft.jeaf.view.viewcustomize.model;


/**
 * 
 * @author linchuan
 *
 */
public class ColumnCustom {
	private String columnName; //列名称
	private String columnTitle; //列标题
	private String columnWidth; //列宽度
	private String columnAlign; //列对齐方式
	
	/**
	 * @return the columnName
	 */
	public String getColumnName() {
		return columnName;
	}
	/**
	 * @param columnName the columnName to set
	 */
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	/**
	 * @return the columnTitle
	 */
	public String getColumnTitle() {
		return columnTitle;
	}
	/**
	 * @param columnTitle the columnTitle to set
	 */
	public void setColumnTitle(String columnTitle) {
		this.columnTitle = columnTitle;
	}
	/**
	 * @return the columnWidth
	 */
	public String getColumnWidth() {
		return columnWidth;
	}
	/**
	 * @param columnWidth the columnWidth to set
	 */
	public void setColumnWidth(String columnWidth) {
		this.columnWidth = columnWidth;
	}
	/**
	 * @return the columnAlign
	 */
	public String getColumnAlign() {
		return columnAlign;
	}
	/**
	 * @param columnAlign the columnAlign to set
	 */
	public void setColumnAlign(String columnAlign) {
		this.columnAlign = columnAlign;
	}
}