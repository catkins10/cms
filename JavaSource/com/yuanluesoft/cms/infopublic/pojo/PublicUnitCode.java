package com.yuanluesoft.cms.infopublic.pojo;

import com.yuanluesoft.jeaf.database.Record;

/**
 * 信息公开:发布机构编号(public_unit_code)
 * @author linchuan
 *
 */
public class PublicUnitCode extends Record {
	private long directoryId; //信息公开目录ID
	private long unitId; //发布机构ID
	private String unitName; //发布机构名称
	private String code; //编号
	
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the directoryId
	 */
	public long getDirectoryId() {
		return directoryId;
	}
	/**
	 * @param directoryId the directoryId to set
	 */
	public void setDirectoryId(long directoryId) {
		this.directoryId = directoryId;
	}
	/**
	 * @return the unitId
	 */
	public long getUnitId() {
		return unitId;
	}
	/**
	 * @param unitId the unitId to set
	 */
	public void setUnitId(long unitId) {
		this.unitId = unitId;
	}
	/**
	 * @return the unitName
	 */
	public String getUnitName() {
		return unitName;
	}
	/**
	 * @param unitName the unitName to set
	 */
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
}